    private void chargeTable() throws SQLException{
        try{
            DefaultTableModel model = new DefaultTableModel();
            userTable.setModel(model);
            Connection connection = con.open();
            PreparedStatement userSel = connection.prepareStatement("Select id,name,permission from users");

            ResultSet data = userSel.executeQuery();
            ResultSetMetaData metadata = userSel.getMetaData();
            int numCol = metadata.getColumnCount();
            model.addColumn("ID");
            model.addColumn("Nombre");
            model.addColumn("Permiso");
            userTable.getColumnModel().getColumn(0).setPreferredWidth(20);
            userTable.getColumnModel().getColumn(1).setPreferredWidth(150);
            userTable.getColumnModel().getColumn(2).setPreferredWidth(100);
            while(data.next()){
                Object row[] = new Object[3];
                for(int i=0;i<3;i++){
                    row[i] = data.getObject(i+1);
                }
                model.addRow(row);
            }
            con.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error: "+e);
        }
    }
    private void addUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserBtnActionPerformed
        if(idUserText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Id vacio");
            return;
        }
        if(nameUserText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Nombre vacio");
            return;
        }
        if(pwdUserText.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Contrase√±a vacia");
            return;
        }
        try{
            Connection connection = con.open();
            PreparedStatement addUser = connection.prepareStatement("insert into users values(?,?,?,?)");
            addUser.setString(1, idUserText.getText().trim().toUpperCase());
            addUser.setString(2, nameUserText.getText().trim().toUpperCase());
            addUser.setString(3, pwdUserText.getText().trim());
            addUser.setString(4, permUserBox.getSelectedItem().toString().toUpperCase());
            addUser.executeUpdate();
            JOptionPane.showMessageDialog(null, "Usuario a√±adido");
            chargeTable();
            cleanUserBtnActionPerformed(evt);
            con.close();
            
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null,"Error: "+e);
        }
    }//GEN-LAST:event_addUserBtnActionPerformed

    private void delUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delUserBtnActionPerformed
        if(userTable.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null,"Selecciona una fila");
            return;
        }
        String permUser = userTable.getModel().getValueAt(userTable.getSelectedRow(), 2).toString();
        if(permUser.equals("JEFE(PELIGRO)")){
            JOptionPane.showMessageDialog(null, "No se puede borrar este usuario");
            return;
        }
        try{
            Connection connection = con.open();
            PreparedStatement delUser = connection.prepareStatement("delete from users where id = ?");
            delUser.setString(1, userTable.getModel().getValueAt(userTable.getSelectedRow(), 0).toString());
            
            delUser.executeUpdate();
            JOptionPane.showMessageDialog(null, "Usuario Eliminado");
            chargeTable();
            con.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error: " + e);

        }
    }//GEN-LAST:event_delUserBtnActionPerformed

